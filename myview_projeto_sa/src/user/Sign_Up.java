   /*
    * Função utilizada para cadastrar um novo usuário no tabela de usuário(tb_user). 
    */
    public void SignUp (){
        String sql = "INSERT INTO tb_user (name_user, email_user, password_user, profile_user, cpf_user) VALUES (?,?,?,?,?);";
        try{
            pst = connection.prepareStatement(sql);
            pst.setString(1,CadName.getText());
            pst.setString(2,CadEmail.getText());
            pst.setString(3,CadPassword.getText());
            pst.setString(4,CadProfile.getSelectedItem().toString());
            pst.setString(5,CadCpf.getText());
            
            if (CadName.getText().isEmpty() || CadEmail.getText().isEmpty() || CadPassword.getText().isEmpty() || CadCpf.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos");
                CadName.grabFocus();
            }
            else{
                int add = pst.executeUpdate();

                if (add > 0){
                    JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso");
                    CadName.setText(null);
                    CadEmail.setText(null);
                    CadPassword.setText(null);
                    CadCpf.setText(null);
                    CadName.grabFocus();
                }
            }
        }
        catch (Exception e){
            System.out.println(e);
        }
    }

