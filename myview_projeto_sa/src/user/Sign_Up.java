    private void CadPasswordKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_CadPasswordKeyTyped
        /*
         * Evento utilizado para limitar o tamanho da senha que o usuário pode escrever no campo texto. 
        */ 
        Character ch = evt.getKeyChar();
        int QtdDigitos = CadPassword.getText().length();
        if (QtdDigitos >= 8){
            evt.consume();
        }
    }//GEN-LAST:event_CadPasswordKeyTyped

